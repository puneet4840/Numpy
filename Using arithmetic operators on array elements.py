# We can use arithmetic operators on each elements of array.
print()

import numpy as np

a=np.arange(1,11)
print(a)
print()

print(a+2)    # add 2 to each element
print()
print(a+a)    # add itself to each element
print()
print(a-1)    # subtracting 1 from each element
print()
print(a*5)    # multiply 5 to each element
print()
print(a/2)    # divide each element by 2
print()
print(a**10)  # power 10 to each element